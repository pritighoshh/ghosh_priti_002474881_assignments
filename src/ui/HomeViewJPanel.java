    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblHomeAddress.getModel();
        model.setRowCount(0);
        
        for(Home hm : info.getInfo()){
            
            Object[] row = new Object[5];
            row[0] = hm;
            row[1] = hm.getCity();
            row[2] = hm.getState();
            row[3] = hm.getZipcode();
            row[4] = hm.getCountry();
            
            model.addRow(row);
            
        }
    }
