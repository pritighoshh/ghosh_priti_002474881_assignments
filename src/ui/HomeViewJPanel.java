    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblHomeAddress.getSelectedRow();

        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblHomeAddress.getModel();
        Home selectedPersons = (Home) model.getValueAt(selectedRowIndex, 0);

        txtApartment.setText(selectedPersons.getApartment());
        txtCity.setText(selectedPersons.getCity());
        txtState.setText(selectedPersons.getState());
        txtZipcode.setText(selectedPersons.getZipcode());
        txtCountry.setText(selectedPersons.getCountry());

    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblHomeAddress.getSelectedRow();

        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblHomeAddress.getModel();
        Home selectedHomes = (Home) model.getValueAt(selectedRowIndex, 0);

        info.deleteHomes(selectedHomes);
        JOptionPane.showMessageDialog(this, "Home Address deleted.");

        populateTable();

    }//GEN-LAST:event_btnDeleteActionPerformed

